package com.cai310.lottery.support;

public class ExecuteException extends Exception {
	private static final long serialVersionUID = 5975441108334476808L;

	public ExecuteException() {
		super();
	}

	public ExecuteException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExecuteException(String arg0) {
		super(arg0);
	}

	public ExecuteException(Throwable arg0) {
		super(arg0);
	}
}
